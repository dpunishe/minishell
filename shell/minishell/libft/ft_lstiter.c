#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	aux = lst;
	if (!lst || !f)
		return ;
	while (aux)
	{
		f(aux->content);
		aux = aux->next;
	}
}
